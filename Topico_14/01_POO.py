"""
Programação orientada a objetos (POO)

 - É um paradigma de programação que utiliza de mapeamento
 de objetos do mundo real para modelos computacionais;

 -Paradigma de programação é a forma/metodologia utilizada
 para pensar e desenvolver sistemas.

Principais elementos da POO:

 Classe -> Modelo do objeto do mundo real sendo reperesentado computacionalmente;
 Atributo -> Característica do objeto;
 Método -> Comportamento do objeto (função dentro de uma classe);
 Construtor -> Método especial utilizado para criar o objeto;
 Objeto -> Instância da classe
"""
